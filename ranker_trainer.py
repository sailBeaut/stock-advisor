"""
ranker_trainer.py

Trains an XGBRanker to rank stocks within each date by their next-30-day
forward return.  Kept alongside trainer.py for A/B comparison.

Design
------
* Each unique date is a "query" (group) for the ranker.
* The relevance label for each (ticker, date) row is the rank percentile
  of its forward_return within that date, scaled to [0, 1000] and cast to
  int.  Higher label = stock had a better realized return.
* XGBRanker optimises NDCG so it learns the *ordering*, not a category.
* At inference, sort by predicted score within each date; take top-N.
  No probability threshold is needed.

Anti-bias guarantees
--------------------
* All chronological splits respect date boundaries — no date spans two splits.
* Group sizes are computed AFTER sorting by date (ascending) and verified with
  assert sum(groups) == len(df) before every fit() call.
* forward_return comes from the labels table which was generated by
  labeler.py using shift(-30) — the ONLY sanctioned negative shift.
"""

import logging
from pathlib import Path

import joblib
import numpy as np
import pandas as pd
from xgboost import XGBRanker

import backtest
import database
from encoders import SECTOR_ENCODER_PATH

